package poly.dto;

public class UserDTO {

	private String email; 
	private String password;
	private String passwordSec;
	private String name;
	private String hp;
	private String birth;
	private String birthSec;
	private String userSeq;
	private String regDate;
	
	
	
	
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getUserSeq() {
		return userSeq;
	}
	public void setUserSeq(String userSeq) {
		this.userSeq = userSeq;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPasswordSec() {
		return passwordSec;
	}
	public void setPasswordSec(String passwordSec) {
		this.passwordSec = passwordSec;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getBirthSec() {
		return birthSec;
	}
	public void setBirthSec(String birthSec) {
		this.birthSec = birthSec;
	}
	
	
	
	
}
